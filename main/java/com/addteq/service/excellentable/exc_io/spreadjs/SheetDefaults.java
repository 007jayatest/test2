package com.addteq.service.excellentable.exc_io.spreadjs;

public class SheetDefaults {
	
	private int rowHeight = 25;
	private int colWidth = 150;
	private int rowHeaderColWidth = 40;
	private int colHeaderRowHeight = 20;
	
	public int getRowHeight() {
		return rowHeight;
	}
	public void setRowHeight(int rowHeight) {
		this.rowHeight = rowHeight;
	}
	public int getColWidth() {
		return colWidth;
	}
	public void setColWidth(int colWidth) {
		this.colWidth = colWidth;
	}
	public int getRowHeaderColWidth() {
		return rowHeaderColWidth;
	}
	public void setRowHeaderColWidth(int rowHeaderColWidth) {
		this.rowHeaderColWidth = rowHeaderColWidth;
	}
	public int getColHeaderRowHeight() {
		return colHeaderRowHeight;
	}
	public void setColHeaderRowHeight(int colHeaderRowHeight) {
		this.colHeaderRowHeight = colHeaderRowHeight;
	}
	
	
	 

}
